import React, { useEffect, useState } from 'react';
import { getCartByUserId } from '../../api/Cart/cart.api';
import { createOrder } from '../../api/Order/order.api';
import Toast from "../../Components/Toast";
import { getUserById, getUserLocations } from '../../api/User/user.api';
import diachiData from '../../assets/data/vietnam_administrative_data.json';
// import { useNavigate } from "react-router-dom";
const Order = () => {
    const [cartItems, setCartItems] = useState([]);
    // const navigate = useNavigate();
    const [form, setForm] = useState({
        hoten: "",
        sodienthoai: "",
        diachinguoidung: "",
        ghichu: "",
        phuongthucthanhtoan: "cod"
    });
    const [toast, setToast] = useState({ show: false, message: "", type: "" });
    const [loading, setLoading] = useState(true);
    const [addressList, setAddressList] = useState([]);

    const userData = localStorage.getItem('user');
    const userId = userData ? JSON.parse(userData)._id : null;

    // H√†m chuy·ªÉn m√£ sang t√™n
    const getCityName = (cityCode) => {
        const city = diachiData.find(c => c.Id === cityCode);
        return city ? city.Name : cityCode;
    };

    const getDistrictName = (cityCode, districtCode) => {
        const city = diachiData.find(c => c.Id === cityCode);
        if (!city) return districtCode;
        const district = city.Districts.find(d => d.Id === districtCode);
        return district ? district.Name : districtCode;
    };

    // L·∫•y th√¥ng tin user, cart v√† ƒë·ªãa ch·ªâ
    useEffect(() => {
        if (userId) {
            getUserById(userId)
                .then(res => {
                    const user = res.data;
                    setForm(prev => ({
                        ...prev,
                        hoten: user.hoten || "",
                        sodienthoai: user.sodienthoai || "",
                        diachinguoidung: user.diachinguoidung || ""
                    }));
                })
                .catch(() => { });

            getCartByUserId(userId)
                .then(res => {
                    setCartItems(res.data || []);
                    setLoading(false);
                })
                .catch(() => setLoading(false));

            getUserLocations(userId)
                .then(res => {
                    setAddressList(res.data || []);
                })
                .catch(() => setAddressList([]));
        }
    }, [userId]);

    const total = cartItems.reduce((sum, item) => sum + (item.giasanpham || 0) * (item.soluong || 1), 0);

    const handleChange = (e) => {
        const { name, value } = e.target;

        if (name === "sodienthoai") {
            // ‚úÖ Ch·ªâ gi·ªØ l·∫°i s·ªë (lo·∫°i b·ªè ch·ªØ t·ª± ƒë·ªông)
            const onlyNumbers = value.replace(/\D/g, "");

            // ‚úÖ Gi·ªõi h·∫°n 10 s·ªë
            if (onlyNumbers.length <= 10) {
                setForm({ ...form, [name]: onlyNumbers });
            }
            return;
        }

        setForm({ ...form, [name]: value });
    };

    // Khi ch·ªçn ƒë·ªãa ch·ªâ c√≥ s·∫µn
    const handleLocationChange = (e) => {
        const selectedId = e.target.value;
        const selected = addressList.find(addr => addr._id === selectedId);
        setForm(prev => ({
            ...prev,
            diachinguoidung: selected
                ? `${getCityName(selected.thanhpho)}, ${getDistrictName(selected.thanhpho, selected.quan_huyen)}, ${selected.diachi}`
                : ""
        }));
    };
    const validatePhone = (phone) => /^[0-9]{10}$/.test(phone);

    const handleOrder = async (e) => {
        e.preventDefault();

        // ‚úÖ Ki·ªÉm tra s·ªë ƒëi·ªán tho·∫°i
        if (!validatePhone(form.sodienthoai)) {
            setToast({ show: true, message: "‚ùå S·ªë ƒëi·ªán tho·∫°i ph·∫£i g·ªìm ƒë√∫ng 10 ch·ªØ s·ªë!", type: "error" });
            return;
        }

        // ‚úÖ Ki·ªÉm tra gi·ªè h√†ng
        if (cartItems.length === 0) {
            setToast({ show: true, message: "Gi·ªè h√†ng c·ªßa b·∫°n ƒëang tr·ªëng!", type: "error" });
            return;
        }

        // ‚úÖ Ki·ªÉm tra th√¥ng tin b·∫Øt bu·ªôc
        if (!form.hoten || !form.diachinguoidung) {
            setToast({ show: true, message: "Vui l√≤ng nh·∫≠p ƒë·∫ßy ƒë·ªß th√¥ng tin!", type: "error" });
            return;
        }

        // üü¢ T·∫°o payload cho API
        const orderData = {
            manguoidung: userId,
            hoten: form.hoten,
            sodienthoai: form.sodienthoai,
            diachinguoidung: form.diachinguoidung,
            ghichu: form.ghichu,
            tongtien: total,
            phuongthucthanhtoan: form.phuongthucthanhtoan,
            chitiet: cartItems.map(item => {
                const base = {
                    masanpham: item.masanpham,
                    tensanpham: item.tensanpham,
                    soluong: item.soluong,
                    giatien: item.giasanpham,
                    size: item.size,
                    mausac: item.mausac,
                    isThietKe: !!item.madesign,
                    madesign: item.madesign || null,
                };

                if (item.hinhanhFront || item.hinhanhBack) {
                    return {
                        ...base,
                        hinhanhFront: item.hinhanhFront || null,
                        hinhanhBack: item.hinhanhBack || null,
                        hinhanh: null
                    };
                }

                return {
                    ...base,
                    hinhanh: item.hinhanh || null,
                    hinhanhFront: null,
                    hinhanhBack: null
                };
            })
        };

        try {
            const res = await createOrder(orderData);
            console.log("‚úÖ ƒê·∫∑t h√†ng th√†nh c√¥ng:", res);

            setToast({ show: true, message: "ƒê·∫∑t h√†ng th√†nh c√¥ng!", type: "success" });

            // üßπ D·ªçn gi·ªè h√†ng
            setCartItems([]);

            // üßπ Reset ghi ch√∫
            setForm(prev => ({ ...prev, ghichu: "" }));

        } catch (error) {
            console.error("‚ùå ƒê·∫∑t h√†ng th·∫•t b·∫°i:", error);
            setToast({ show: true, message: "ƒê·∫∑t h√†ng th·∫•t b·∫°i!", type: "error" });
        }
    };


    const openBase64Image = (base64Data) => {
        // T√°ch header (data:image/png;base64,) v√† ph·∫ßn base64 thu·∫ßn
        const arr = base64Data.split(",");
        const mime = arr[0].match(/:(.*?);/)[1];
        const bstr = atob(arr[1]);
        let n = bstr.length;
        const u8arr = new Uint8Array(n);

        while (n--) {
            u8arr[n] = bstr.charCodeAt(n);
        }

        // T·∫°o Blob v√† URL t·∫°m
        const blob = new Blob([u8arr], { type: mime });
        const url = URL.createObjectURL(blob);

        // M·ªü ·∫£nh trong tab m·ªõi
        window.open(url, "_blank");

        // (Tu·ª≥ ch·ªçn) Gi·∫£i ph√≥ng URL sau 1 ph√∫t cho an to√†n
        setTimeout(() => URL.revokeObjectURL(url), 60000);
    };

    return (
        <div className="container mt-4">
            <Toast
                show={toast.show}
                message={toast.message}
                type={toast.type}
                onClose={() => setToast({ ...toast, show: false })}
            />
            <div className="row">
                {/* B√™n tr√°i: S·∫£n ph·∫©m */}
                <div className="col-md-7">
                    <div className="card mb-3">
                        <div className="card-header fw-bold">S·∫£n ph·∫©m trong gi·ªè h√†ng</div>
                        <div className="card-body">
                            {loading ? (
                                <p>ƒêang t·∫£i...</p>
                            ) : cartItems.length === 0 ? (
                                <p>Kh√¥ng c√≥ s·∫£n ph·∫©m n√†o trong gi·ªè h√†ng.</p>
                            ) : (
                                <table className="table table-bordered align-middle">
                                    <thead>
                                        <tr className="text-center">
                                            <th>H√¨nh ·∫£nh</th>
                                            <th>T√™n s·∫£n ph·∫©m</th>
                                            <th>Size</th>
                                            <th>M√†u</th>
                                            <th>S·ªë l∆∞·ª£ng</th>
                                            <th>Gi√°</th>
                                        </tr>
                                    </thead>
                                    <tbody className="text-center">
                                        {cartItems.map((item, idx) => (
                                            <tr key={item._id || idx}>
                                                <td style={{ width: 120, height: 60 }}>
                                                    <div style={{ display: "flex", width: "100%", height: "100%", gap: 4 }}>
                                                        {/* N·∫øu c√≥ front/back ‚Üí s·∫£n ph·∫©m custom */}
                                                        {item.hinhanhFront || item.hinhanhBack ? (
                                                            <>
                                                                {/* Front */}
                                                                <img
                                                                    src={item.hinhanhFront}
                                                                    alt="Front"
                                                                    style={{
                                                                        width: "50%",
                                                                        height: "100%",
                                                                        objectFit: "cover",
                                                                        border: "1px solid #ddd",
                                                                        borderRadius: 4,
                                                                        cursor: "zoom-in",
                                                                    }}
                                                                    onClick={(e) => {
                                                                        e.stopPropagation();
                                                                        openBase64Image(item.hinhanhFront);
                                                                    }}
                                                                />

                                                                {/* Back (n·∫øu c√≥) */}
                                                                {item.hinhanhBack && (
                                                                    <img
                                                                        src={item.hinhanhBack}
                                                                        alt="Back"
                                                                        style={{
                                                                            width: "50%",
                                                                            height: "100%",
                                                                            objectFit: "cover",
                                                                            border: "1px solid #ddd",
                                                                            borderRadius: 4,
                                                                            cursor: "zoom-in",
                                                                        }}
                                                                        onClick={(e) => {
                                                                            e.stopPropagation();
                                                                            openBase64Image(item.hinhanhBack);
                                                                        }}
                                                                    />
                                                                )}
                                                            </>
                                                        ) : (
                                                            /* N·∫øu ch·ªâ c√≥ ·∫£nh s·∫£n ph·∫©m ti√™u chu·∫©n */
                                                            <img
                                                                src={item.hinhanh}
                                                                alt={item.tensanpham}
                                                                style={{
                                                                    width: "50%",
                                                                    height: "100%",
                                                                    objectFit: "cover",
                                                                    border: "1px solid #ddd",
                                                                    borderRadius: 4,
                                                                    cursor: "zoom-in",
                                                                }}
                                                                onClick={(e) => {
                                                                    e.stopPropagation();
                                                                    openBase64Image(item.hinhanh);
                                                                }}
                                                            />
                                                        )}
                                                    </div>
                                                </td>
                                                <td>{item.tensanpham}</td>
                                                <td>{item.size}</td>
                                                <td>
                                                    <span style={{
                                                        display: "inline-block",
                                                        width: 20,
                                                        height: 20,
                                                        background: item.mausac,
                                                        border: "1px solid #ccc",
                                                        borderRadius: 4
                                                    }} title={item.mausac}></span>
                                                </td>
                                                <td>{item.soluong}</td>
                                                <td>{(item.giasanpham || 0).toLocaleString("vi-VN")} ƒë</td>
                                            </tr>
                                        ))}
                                    </tbody>
                                </table>
                            )}
                            <div className="text-end fw-bold mt-2">
                                T·ªïng ti·ªÅn: {total.toLocaleString("vi-VN")} ƒë
                            </div>
                        </div>
                    </div>
                </div>
                {/* B√™n ph·∫£i: Th√¥ng tin ƒë·∫∑t h√†ng */}
                <div className="col-md-5">
                    <div className="card" style={{ position: "sticky", top: "80px", zIndex: 10 }}>
                        <div className="card-header fw-bold">Th√¥ng tin ƒë·∫∑t h√†ng</div>
                        <div className="card-body">
                            <form onSubmit={handleOrder}>
                                <div className="mb-3">
                                    <label className="form-label">H·ªç t√™n</label>
                                    <input
                                        type="text"
                                        className="form-control"
                                        name="hoten"
                                        value={form.hoten}
                                        onChange={handleChange}
                                        placeholder="Nh·∫≠p h·ªç t√™n"
                                    />
                                </div>
                                <input
                                    type="text"
                                    className="form-control"
                                    name="sodienthoai"
                                    value={form.sodienthoai}
                                    onChange={handleChange}
                                    inputMode="numeric"     // ‚ö° G·ª£i √Ω b√†n ph√≠m s·ªë tr√™n mobile
                                    placeholder="Nh·∫≠p s·ªë ƒëi·ªán tho·∫°i 10 s·ªë"
                                />

                                <div className="mb-3">
                                    <label className="form-label">Ch·ªçn ƒë·ªãa ch·ªâ c√≥ s·∫µn</label>
                                    <select
                                        className="form-select"
                                        onChange={handleLocationChange}
                                        value={
                                            addressList.find(addr =>
                                                `${getCityName(addr.thanhpho)}, ${getDistrictName(addr.thanhpho, addr.quan_huyen)}, ${addr.diachi}` === form.diachinguoidung
                                            )?._id || ""
                                        }
                                    >
                                        <option value="">-- Ch·ªçn ƒë·ªãa ch·ªâ --</option>
                                        {addressList.map(addr => (
                                            <option key={addr._id} value={addr._id}>
                                                {getCityName(addr.thanhpho)}, {getDistrictName(addr.thanhpho, addr.quan_huyen)}, {addr.diachi}
                                            </option>
                                        ))}
                                    </select>

                                    {/* Hidden input d√πng ƒë·ªÉ gi·ªØ gi√° tr·ªã ƒë·ªãa ch·ªâ */}
                                    <input
                                        type="hidden"
                                        className="form-control"
                                        name="diachinguoidung"
                                        value={form.diachinguoidung}
                                        onChange={handleChange}
                                        placeholder="Nh·∫≠p ƒë·ªãa ch·ªâ giao h√†ng"
                                    />
                                </div>

                                <div className="mb-3">
                                    <label className="form-label">Ghi ch√∫</label>
                                    <textarea
                                        className="form-control"
                                        name="ghichu"
                                        value={form.ghichu}
                                        onChange={handleChange}
                                        placeholder="Nh·∫≠p ghi ch√∫"
                                    />
                                </div>

                                <div className="mb-3">
                                    <label className="form-label">Ph∆∞∆°ng th·ª©c thanh to√°n</label>
                                    <select
                                        className="form-select"
                                        name="phuongthucthanhtoan"
                                        value={form.phuongthucthanhtoan}
                                        onChange={handleChange}
                                    >
                                        <option value="cod">Thanh to√°n khi nh·∫≠n h√†ng (COD)</option>
                                        <option value="bank">Chuy·ªÉn kho·∫£n ng√¢n h√†ng</option>
                                    </select>
                                </div>

                                <button
                                    type="submit"
                                    className="btn btn-success w-100"
                                    disabled={cartItems.length === 0}
                                >
                                    ƒê·∫∑t h√†ng
                                </button>
                            </form>
                        </div>
                    </div>
                </div>
            </div>
        </div>
    );
};

export default Order;